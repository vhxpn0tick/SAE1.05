def count_dynamic_ip_occurrences(log_contents):

    ip_pattern = re.compile(r"Source = (\S+);|Destination = (\S+);")
    
    ip_count = defaultdict(int)
    
    matches = ip_pattern.findall(log_contents)
    
    for match in matches:
        for ip in match:
            if ip: 
                ip_count[ip] += 1
    
    return ip_count

